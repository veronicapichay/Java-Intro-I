class PrimitivesTest {

    public static void main(String[] args) {

        int age = 34;
        System.out.println("The age is " + age);

        long population = 8_000_000_000L;
        System.out.println("The human population of the earth is " +  population);

        double bladeLength = 11.5;
        System.out.println("The blade length of my sword is " + bladeLength);

        boolean isStressed = false;
        System.out.println("Am I stressed? " + isStressed);

        char size = 'V';
        System.out.println("The size is " + size);

        String grade = "A";
        System.out.println("Everyone is the class received a grade of " + grade);

        String quote = "What's up, \"Doc\"?";
        System.out.println(quote);
    }
}
